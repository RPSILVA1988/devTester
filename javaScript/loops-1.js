// FOR (é formado por três partes)

//Inicialização onde i começa com ZERO
//Condição, executa o bloco de código enquanto i for menor ou igual a 10
//Incremento, soma 1 a variável i a cada rodada, para não ficar em Looping infinito

for (var i = 0; i <= 10; i++) {
    console.log(`Devo ver essa mensagem enquanto ${i} for menor ou igual a 10`)
}